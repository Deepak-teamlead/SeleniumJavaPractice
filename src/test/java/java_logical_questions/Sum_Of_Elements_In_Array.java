package java_logical_questions;
public class Sum_Of_Elements_In_Array {
    public static void main(String[] args) {
        int a[]={1,2,3};
        int sum=0;
        for(int i=0;i<a.length;i++){
            sum=sum+a[i];
        }
        System.out.println("Addition of elements in array is :"+sum);

    }

}
