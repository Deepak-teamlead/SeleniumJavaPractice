package java_practice_1;

public class Switch_Case_Condition {
public static void main(String[] args) {
//Consider Range Of Variable is 1to5
 int a=3;
switch(a) {
case 1: 
	System.out.println("variable value is 1");
     break;
case 2: 
	System.out.println("variable value is 2");
     break;
case 3: 
	System.out.println("variable value is 3");
     break;     
case 4: 
	System.out.println("variable value is 4");
     break;       
case 5: 
	System.out.println("variable value is 5");
     break;  	
default:
	System.out.println("Variable value is out of range");
}
}
}
